package com.marcelo.variaveisinteiras;

public class VariaveisInteiras {

    public static void main(String[] args) {
        byte numero01 = 20;
        short numero02 = 20;
        int numero03 = 20;
        long numero04 = 20;
        
        System.out.println("primeira idade em byte " + numero01);
        System.out.println("primeira idade em short " + numero02);
        System.out.println("primeira idade em int " + numero03);
        System.out.println("primeira idade em long " + numero04);
            
    }
}
