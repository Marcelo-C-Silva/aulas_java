
package com.marcelo.operadoresaritmeticos;


public class OperadoresAritmeticos {

    public static void main(String[] args) {
        int soma = 1 + 2;
        
        System.out.println(soma);
        
        soma = soma - 1;
        
        System.out.println(soma);
        
        soma = soma * 2;
        
        System.out.println(soma);
        
        soma = soma / 2;
        
        System.out.println(soma);
        
    }
}
